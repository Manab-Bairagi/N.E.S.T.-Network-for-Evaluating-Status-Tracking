class EnhancedFeatureExtractor(FeatureExtractor):
    """
    Enhanced feature extractor with:
    - Multi-scale feature extraction for low resolution
    - Appearance buffer for re-identification
    - Quality-aware feature extraction
    """

    def __init__(self, model_path='best_model.pth', device='cuda',
                 buffer_size=100, min_feature_quality=0.3):
        super().__init__(model_path, device)

        # Appearance buffer for re-ID
        self.appearance_buffer = []  # List of (feature, track_id, timestamp)
        self.buffer_size = buffer_size
        self.min_feature_quality = min_feature_quality

        # Multi-scale extraction for low-res robustness
        self.use_multiscale = True

    def assess_crop_quality(self, crop):
        """
        Assess if crop is good quality for feature extraction
        Returns quality score 0-1
        """
        if crop.size == 0:
            return 0.0

        h, w = crop.shape[:2]

        # Check minimum size
        if h < 40 or w < 20:
            return 0.1

        # Check aspect ratio (person should be ~2:1)
        aspect = h / (w + 1e-6)
        aspect_score = 1.0 - abs(aspect - 2.0) / 2.0
        aspect_score = max(0, min(1, aspect_score))

        # Check blurriness using Laplacian variance
        gray = cv2.cvtColor(crop, cv2.COLOR_BGR2GRAY)
        laplacian_var = cv2.Laplacian(gray, cv2.CV_64F).var()
        blur_score = min(1.0, laplacian_var / 100.0)

        # Check brightness
        mean_brightness = np.mean(gray)
        brightness_score = 1.0 - abs(mean_brightness - 128) / 128

        # Combined quality score
        quality = (aspect_score * 0.4 + blur_score * 0.4 + brightness_score * 0.2)

        return quality

    def extract_multiscale(self, crop):
        """Extract features at multiple scales for robustness"""
        features_list = []

        # Original scale
        crop_resized = cv2.resize(crop, (self.size[1], self.size[0]))
        crop_rgb = cv2.cvtColor(crop_resized, cv2.COLOR_BGR2RGB)
        crop_tensor = torch.from_numpy(crop_rgb).permute(2, 0, 1).float() / 255.0
        crop_tensor = (crop_tensor.unsqueeze(0).to(self.device) - self.mean) / self.std

        with torch.no_grad():
            feat = self.model(crop_tensor, return_feature=True)
            features_list.append(feat)

        # If using multiscale and crop is low quality, add augmented versions
        quality = self.assess_crop_quality(crop)

        if quality < 0.6 and self.use_multiscale:
            # Slightly zoomed in (focuses on torso)
            h, w = crop.shape[:2]
            crop_zoom = crop[int(h*0.1):int(h*0.9), int(w*0.1):int(w*0.9)]
            if crop_zoom.size > 0:
                crop_zoom_resized = cv2.resize(crop_zoom, (self.size[1], self.size[0]))
                crop_zoom_rgb = cv2.cvtColor(crop_zoom_resized, cv2.COLOR_BGR2RGB)
                crop_zoom_tensor = torch.from_numpy(crop_zoom_rgb).permute(2, 0, 1).float() / 255.0
                crop_zoom_tensor = (crop_zoom_tensor.unsqueeze(0).to(self.device) - self.mean) / self.std

                with torch.no_grad():
                    feat_zoom = self.model(crop_zoom_tensor, return_feature=True)
                    features_list.append(feat_zoom)

        # Average features from multiple scales
        features = torch.mean(torch.stack(features_list), dim=0)
        return features

    @torch.no_grad()
    def extract(self, img, bboxes, return_quality=False):
        """Extract features with quality assessment"""
        if len(bboxes) == 0:
            return np.empty((0, 128))

        crops = []
        qualities = []

        for bbox in bboxes:
            x1, y1, x2, y2 = map(int, bbox)
            x1, y1 = max(0, x1), max(0, y1)
            x2, y2 = min(img.shape[1], x2), min(img.shape[0], y2)
            crop = img[y1:y2, x1:x2]

            if crop.size > 0:
                crops.append(crop)
                quality = self.assess_crop_quality(crop)
                qualities.append(quality)
            else:
                crops.append(np.zeros((64, 64, 3), dtype=np.uint8))
                qualities.append(0.0)

        # Extract features
        features_list = []
        for crop, quality in zip(crops, qualities):
            if quality > self.min_feature_quality:
                feat = self.extract_multiscale(crop)
            else:
                # For very low quality, use standard extraction
                batch = self.preprocess([crop])
                feat = self.model(batch, return_feature=True)

            features_list.append(feat)

        features = torch.cat(features_list, dim=0).cpu().numpy()

        if return_quality:
            return features, qualities
        return features

    def add_to_buffer(self, feature, track_id, timestamp):
        """Add feature to appearance buffer for re-ID"""
        self.appearance_buffer.append({
            'feature': feature,
            'track_id': track_id,
            'timestamp': timestamp
        })

        # Keep buffer size limited
        if len(self.appearance_buffer) > self.buffer_size:
            self.appearance_buffer.pop(0)

    def search_buffer(self, feature, max_age=120, threshold=0.5):
        """Search buffer for matching track"""
        if len(self.appearance_buffer) == 0:
            return None

        best_match = None
        best_similarity = threshold

        for entry in reversed(self.appearance_buffer):
            # Skip if too old
            if entry['timestamp'] < (len(self.appearance_buffer) - max_age):
                continue

            # Calculate similarity
            feature = feature / (np.linalg.norm(feature) + 1e-6)
            stored = entry['feature'] / (np.linalg.norm(entry['feature']) + 1e-6)
            similarity = np.dot(feature, stored)

            if similarity > best_similarity:
                best_similarity = similarity
                best_match = entry['track_id']

        return best_match


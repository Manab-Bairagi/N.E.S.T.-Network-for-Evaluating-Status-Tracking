class NSAKalmanFilter:
    """
    Noise Scale Adaptive (NSA) Kalman Filter.
    Adapts measurement noise (R) based on detection confidence.
    """
    def __init__(self):
        # State: [x, y, w, h, vx, vy, vw, vh]
        self.dt = 1.0

        # 1. Motion Model (Constant Velocity)
        self.F = np.eye(8)
        for i in range(4):
            self.F[i, i+4] = self.dt

        # 2. Measurement Matrix
        self.H = np.eye(4, 8)

        # 3. Standard Measurement Noise (R)
        # We use a base R, but it will be scaled dynamically
        self.std_weight_position = 1.0 / 20
        self.std_weight_velocity = 1.0 / 160

        # 4. Process Noise (Q) - Tuned for smoother tracking during occlusion
        self.Q = np.eye(8)
        # Position noise (allow some jitter)
        self.Q[0:4, 0:4] *= 0.05
        # Velocity noise (assume velocity changes slowly)
        self.Q[4:8, 4:8] *= 0.00625

        # 5. Covariance (P)
        self.P = np.eye(8) * 10

        # State vector
        self.x = np.zeros(8)

    def init(self, measurement):
        """Initialize with [x, y, w, h]"""
        self.x[:4] = measurement
        self.x[4:] = 0

        # Heuristic: Set initial velocity covariance high (we don't know velocity yet)
        self.P[4:, 4:] *= 1000

    def predict(self):
        """
        Predict next state.
        During occlusion, P naturally grows, increasing uncertainty.
        """
        self.x = self.F @ self.x
        self.P = self.F @ self.P @ self.F.T + self.Q
        return self.x[:4]

    def update(self, measurement, confidence=1.0):
        """
        NSA Update: Scales noise R based on detection confidence.

        Args:
            measurement: [x, y, w, h]
            confidence: detection confidence (0.0 to 1.0)
        """
        # 1. Dynamic R Calculation (The NSA Trick)
        # Low confidence -> High R -> Trust Prediction (Handles occlusion/blur)
        # High confidence -> Low R -> Trust Measurement

        # Base noise based on box size (height)
        h = max(measurement[3], 10) # Avoid division by zero
        std_pos = self.std_weight_position * h
        std_vel = self.std_weight_velocity * h

        # Scale factor: If confidence is low (e.g., 0.5), we increase noise
        # Formula: (1 - confidence) * factor.
        # We add a small epsilon to prevent R from being 0.
        inv_conf = 1.0 - confidence
        scale = 1.0 + (inv_conf * 10.0) # Intense scaling for low confidence

        # Construct Adaptive R
        R = np.eye(4)
        R[0, 0] = std_pos ** 2 * scale  # x
        R[1, 1] = std_pos ** 2 * scale  # y
        R[2, 2] = std_pos ** 2 * scale  # w
        R[3, 3] = std_pos ** 2 * scale  # h

        # 2. Standard Kalman Update with Adaptive R
        y = measurement - self.H @ self.x
        S = self.H @ self.P @ self.H.T + R
        K = self.P @ self.H.T @ np.linalg.inv(S)

        self.x = self.x + K @ y
        I = np.eye(8)
        self.P = (I - K @ self.H) @ self.P

        return self.x[:4]

    def get_state(self):
        return self.x[:4]


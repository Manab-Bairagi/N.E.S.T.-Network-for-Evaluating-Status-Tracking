def track_custom_video(video_path, output_path='output_tracked.mp4',
                       conf_threshold=0.5, max_frames=None,
                       model_path='/content/best_model.pth'):
    """Run memory-enhanced BoT-SORT on custom video"""

    print("=" * 80)
    print(f"Running MEMORY-ENHANCED Tracker on: {video_path}")
    print(f"Using model: {model_path}")
    print("=" * 80)

    if not os.path.exists(video_path):
        print(f"Error: Video not found: {video_path}")
        return

    if not os.path.exists(model_path):
        print(f"Error: Model not found: {model_path}")
        return

    cap = cv2.VideoCapture(video_path)
    if not cap.isOpened():
        print("Error: Cannot open video")
        return

    fps = int(cap.get(cv2.CAP_PROP_FPS))
    width = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    height = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
    total_frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))

    if max_frames:
        total_frames = min(total_frames, max_frames)

    print(f"Video: {width}x{height} @ {fps} FPS, {total_frames} frames")

    # Initialize components
    print("\nLoading detector...")
    detector = YOLO('yolo11s.pt')
    print("✓ Detector loaded")

    print(f"Loading feature extractor...")
    feature_extractor = EnhancedFeatureExtractor(
        model_path=model_path,
        device=device,
        buffer_size=500,
        min_feature_quality=0.05
    )
    print("✓ Feature extractor loaded")

    print("Initializing memory-enhanced tracker...")
    tracker = MemoryEnhancedBoTSORT(
        feature_extractor=feature_extractor,
        max_age=600,
        min_hits=5,
        iou_threshold=0.4,
        lambda_iou=0.9,
        lambda_reid=0.1,
        reid_threshold=0.30,
        reid_buffer_size=50,
        memory_threshold=0.45,
        memory_size=200
    )
    print("✓ Tracker initialized with long-term memory")

    fourcc = cv2.VideoWriter_fourcc(*'mp4v')
    out = cv2.VideoWriter(output_path, fourcc, fps, (width, height))

    print("\n" + "=" * 80)
    print("Processing video...")
    print("=" * 80)

    frame_idx = 0

    while True:
        ret, frame = cap.read()
        if not ret or (max_frames and frame_idx >= max_frames):
            break

        # Detect
        results = detector(frame, verbose=False)[0]
        detections = []

        for box in results.boxes:
            if box.cls[0] == 0:  # person
                x1, y1, x2, y2 = box.xyxy[0].cpu().numpy()
                conf = box.conf[0].cpu().numpy()
                if conf > conf_threshold:
                    detections.append([x1, y1, x2, y2, conf])

        detections = np.array(detections) if detections else np.empty((0, 5))

        # Track
        tracks = tracker.update(frame, detections)
        stats = tracker.get_statistics()

        # Visualize
        vis_frame = draw_tracks(frame, tracks, show_trajectory=False)
        vis_frame = draw_trajectories(vis_frame, tracker)

        # Add info
        info1 = f"Frame: {frame_idx+1}/{total_frames} | Active: {len(tracks)} | Memory: {stats['memory_identities']} IDs"
        cv2.putText(vis_frame, info1, (10, 25),
                   cv2.FONT_HERSHEY_SIMPLEX, 0.5, (0, 255, 0), 2)

        info2 = f"Recoveries: {stats['memory_recoveries']} | New IDs: {stats['new_ids_created']}"
        cv2.putText(vis_frame, info2, (10, 50),
                   cv2.FONT_HERSHEY_SIMPLEX, 0.5, (0, 255, 255), 2)

        out.write(vis_frame)

        if (frame_idx + 1) % 30 == 0:
            print(f"  [{frame_idx+1}/{total_frames}] Active: {len(tracks)} | "
                  f"Memory: {stats['memory_identities']} | "
                  f"Recoveries: {stats['memory_recoveries']}")

        frame_idx += 1

    cap.release()
    out.release()

    stats = tracker.get_statistics()

    print("\n" + "=" * 80)
    print("✓ Tracking complete!")
    print(f"✓ Output: {output_path}")
    print(f"✓ Frames: {frame_idx}")
    print(f"✓ Total IDs: {Track._count}")
    print(f"✓ Memory recoveries: {stats['memory_recoveries']}")
    print(f"✓ New IDs created: {stats['new_ids_created']}")
    print("=" * 80)



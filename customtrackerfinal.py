class Track:
    """Individual track with NSA Kalman filter and appearance features."""
    _count = 0

    def __init__(self, bbox, feature, track_id=None):
        """
        Args:
            bbox: [x1, y1, x2, y2]
            feature: appearance feature vector
        """
        self.track_id = Track._count if track_id is None else track_id
        Track._count += 1

        # Convert bbox to [cx, cy, w, h]
        x1, y1, x2, y2 = bbox
        cx = (x1 + x2) / 2
        cy = (y1 + y2) / 2
        w = x2 - x1
        h = y2 - y1

        # Initialize NSA Kalman filter
        # NOTE: Ensure NSAKalmanFilter class (Cell 4) is defined before running this!
        self.kf = NSAKalmanFilter()
        self.kf.init(np.array([cx, cy, w, h]))

        # Appearance features (keep a gallery of recent features)
        self.features = deque([feature], maxlen=30)

        # Track state
        self.hits = 1
        self.age = 1
        self.time_since_update = 0
        self.state = 'tentative'  # tentative, confirmed, deleted

        # Trajectory for visualization
        self.trajectory = deque(maxlen=30)
        self.trajectory.append((cx, cy))

    def predict(self):
        """Predict next position using Kalman filter."""
        self.age += 1
        self.time_since_update += 1
        pred = self.kf.predict()
        return self._xywh_to_xyxy(pred)

    def update(self, bbox, feature, confidence=1.0):
        """
        Update track with new detection.
        Args:
            bbox: [x1, y1, x2, y2]
            feature: appearance feature
            confidence: detection confidence score (for NSA Kalman Filter)
        """
        x1, y1, x2, y2 = bbox
        cx = (x1 + x2) / 2
        cy = (y1 + y2) / 2
        w = x2 - x1
        h = y2 - y1

        # NSA Update: Pass confidence to the Kalman Filter
        self.kf.update(np.array([cx, cy, w, h]), confidence=confidence)

        self.features.append(feature)
        self.trajectory.append((cx, cy))

        self.hits += 1
        self.time_since_update = 0

        if self.state == 'tentative' and self.hits >= 3:
            self.state = 'confirmed'

    def mark_missed(self):
        """Mark track as missed."""
        if self.state == 'tentative':
            self.state = 'deleted'
        elif self.time_since_update > 30:
            self.state = 'deleted'

    def get_state(self):
        """Get current bbox [x1, y1, x2, y2]."""
        xywh = self.kf.get_state()
        return self._xywh_to_xyxy(xywh)

    def get_feature(self):
        """Get averaged appearance feature."""
        return np.mean(self.features, axis=0)

    @staticmethod
    def _xywh_to_xyxy(xywh):
        """Convert [cx, cy, w, h] to [x1, y1, x2, y2]."""
        cx, cy, w, h = xywh
        x1 = cx - w / 2
        y1 = cy - h / 2
        x2 = cx + w / 2
        y2 = cy + h / 2
        return np.array([x1, y1, x2, y2])


class BoTSORT:
    """BoT-SORT tracker with GMC and fused matching."""
    def __init__(self, feature_extractor, max_age=120, min_hits=4,
                 iou_threshold=0.3, lambda_iou=0.98, lambda_reid=0.02):
        """
        Args:
            feature_extractor: FeatureExtractor instance
            max_age: maximum frames to keep alive a track without updates
            min_hits: minimum hits before confirming a track
            iou_threshold: IoU threshold for matching
            lambda_iou: weight for IoU distance in fused cost
            lambda_reid: weight for ReID distance in fused cost
        """
        self.feature_extractor = feature_extractor
        self.max_age = max_age
        self.min_hits = min_hits
        self.iou_threshold = iou_threshold
        self.lambda_iou = lambda_iou
        self.lambda_reid = lambda_reid

        self.tracks = []
        self.frame_count = 0

        # GMC for camera motion compensation
        self.gmc = GMC(method='orb', downscale=2)

    def update(self, frame, detections):
        """
        Update tracker with new detections.
        Args:
            frame: current frame (H, W, 3)
            detections: Nx5 array [x1, y1, x2, y2, conf]
        Returns:
            list of active tracks with [x1, y1, x2, y2, track_id]
        """
        self.frame_count += 1

        # Apply GMC to compensate for camera motion
        transform = self.gmc.apply(frame, detections)
        self._camera_update(transform)

        # Predict all tracks
        for track in self.tracks:
            track.predict()

        # Extract features for detections
        if len(detections) > 0:
            bboxes = detections[:, :4]
            features = self.feature_extractor.extract(frame, bboxes)
            # Extract confidences if available, else default to 1.0
            confidences = detections[:, 4] if detections.shape[1] > 4 else np.ones(len(detections))
        else:
            bboxes = np.empty((0, 4))
            features = np.empty((0, 512))
            confidences = np.empty(0)

        # Match detections to tracks
        matches, unmatched_dets, unmatched_tracks = self._match(
            detections, features
        )

        # Update matched tracks
        for track_idx, det_idx in matches:
            # Pass detection confidence to update method
            self.tracks[track_idx].update(
                bboxes[det_idx],
                features[det_idx],
                confidence=confidences[det_idx]
            )

        # Mark unmatched tracks as missed
        for track_idx in unmatched_tracks:
            self.tracks[track_idx].mark_missed()

        # Create new tracks for unmatched detections
        for det_idx in unmatched_dets:
            new_track = Track(bboxes[det_idx], features[det_idx])
            # Initial update to set confidence
            new_track.update(bboxes[det_idx], features[det_idx], confidence=confidences[det_idx])
            self.tracks.append(new_track)

        # Remove deleted tracks
        self.tracks = [t for t in self.tracks if t.state != 'deleted']

        # Return confirmed tracks
        outputs = []
        for track in self.tracks:
            if track.state == 'confirmed':
                bbox = track.get_state()
                outputs.append([*bbox, track.track_id])

        return np.array(outputs) if outputs else np.empty((0, 5))

    def _camera_update(self, transform):
        """Apply camera motion compensation to all tracks."""
        for track in self.tracks:
            # Get current state
            cx, cy, w, h = track.kf.x[:4]

            # Apply affine transformation to center point
            pt = np.array([[cx, cy]], dtype=np.float32)
            pt_transformed = cv2.transform(pt.reshape(1, 1, 2), transform).reshape(2)

            # Update Kalman filter state
            track.kf.x[0] = pt_transformed[0]
            track.kf.x[1] = pt_transformed[1]

    def _match(self, detections, det_features):
        """Match detections to tracks using fused IoU + ReID cost."""
        if len(self.tracks) == 0 or len(detections) == 0:
            return [], list(range(len(detections))), []

        # Get track predictions and features
        track_bboxes = np.array([t.get_state() for t in self.tracks])
        track_features = np.array([t.get_feature() for t in self.tracks])

        det_bboxes = detections[:, :4]

        # Compute IoU distance matrix
        iou_dist = self._iou_distance(track_bboxes, det_bboxes)

        # Compute cosine distance matrix for ReID
        cosine_dist = self._cosine_distance(track_features, det_features)

        # Fused cost matrix
        cost_matrix = self.lambda_iou * iou_dist + self.lambda_reid * cosine_dist

        # Hungarian algorithm for assignment
        row_ind, col_ind = linear_sum_assignment(cost_matrix)

        matches = []
        unmatched_tracks = list(range(len(self.tracks)))
        unmatched_dets = list(range(len(detections)))

        for r, c in zip(row_ind, col_ind):
            if cost_matrix[r, c] < 0.7:  # threshold for valid match
                matches.append((r, c))
                unmatched_tracks.remove(r)
                unmatched_dets.remove(c)

        return matches, unmatched_dets, unmatched_tracks

    @staticmethod
    def _iou_distance(bboxes1, bboxes2):
        """Compute IoU distance matrix."""
        n = len(bboxes1)
        m = len(bboxes2)

        if n == 0 or m == 0:
            return np.zeros((n, m))

        # Expand dimensions for broadcasting
        b1 = np.expand_dims(bboxes1, 1)  # (n, 1, 4)
        b2 = np.expand_dims(bboxes2, 0)  # (1, m, 4)

        # Intersection
        x1 = np.maximum(b1[..., 0], b2[..., 0])
        y1 = np.maximum(b1[..., 1], b2[..., 1])
        x2 = np.minimum(b1[..., 2], b2[..., 2])
        y2 = np.minimum(b1[..., 3], b2[..., 3])

        inter_area = np.maximum(0, x2 - x1) * np.maximum(0, y2 - y1)

        # Union
        b1_area = (b1[..., 2] - b1[..., 0]) * (b1[..., 3] - b1[..., 1])
        b2_area = (b2[..., 2] - b2[..., 0]) * (b2[..., 3] - b2[..., 1])
        union_area = b1_area + b2_area - inter_area

        iou = inter_area / (union_area + 1e-6)
        return 1 - iou

    @staticmethod
    def _cosine_distance(features1, features2):
        """Compute cosine distance matrix."""
        # Normalize features
        features1 = features1 / (np.linalg.norm(features1, axis=1, keepdims=True) + 1e-6)
        features2 = features2 / (np.linalg.norm(features2, axis=1, keepdims=True) + 1e-6)

        # Cosine similarity
        similarity = np.dot(features1, features2.T)

        # Convert to distance
        return 1 - similarity


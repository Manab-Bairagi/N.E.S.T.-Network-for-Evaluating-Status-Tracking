class EnhancedBoTSORT(BoTSORT):
    """
    Enhanced BoT-SORT with:
    - Re-identification buffer for re-entering persons
    - Better occlusion handling
    - Confidence-based track management
    """

    def __init__(self, feature_extractor, max_age=120, min_hits=3,
                 iou_threshold=0.3, lambda_iou=0.98, lambda_reid=0.02,
                 reid_threshold=0.8, reid_buffer_size=50):

        # Initialize parent class but we'll override some methods
        self.feature_extractor = feature_extractor
        self.max_age = max_age
        self.min_hits = min_hits
        self.iou_threshold = iou_threshold
        self.lambda_iou = lambda_iou
        self.lambda_reid = lambda_reid

        self.tracks = []
        self.frame_count = 0

        # GMC for camera motion compensation
        self.gmc = GMC(method='orb', downscale=2)

        # Re-ID buffer settings
        self.reid_threshold = reid_threshold
        self.reid_buffer_size = reid_buffer_size
        self.lost_tracks = []  # Tracks that were lost but might return

    def update(self, frame, detections):
        """Enhanced update with re-identification"""
        self.frame_count += 1

        # Apply GMC
        transform = self.gmc.apply(frame, detections)
        self._camera_update(transform)

        # Predict all tracks
        for track in self.tracks:
            track.predict()

        # Extract features with quality
        if len(detections) > 0:
            bboxes = detections[:, :4]
            features, qualities = self.feature_extractor.extract(
                frame, bboxes, return_quality=True
            )
        else:
            bboxes = np.empty((0, 4))
            features = np.empty((0, 128))
            qualities = []

        # First matching: active tracks
        matches, unmatched_dets, unmatched_tracks = self._match(
            detections, features, qualities
        )

        # Update matched tracks
        for track_idx, det_idx in matches:
            self.tracks[track_idx].update(
                bboxes[det_idx], features[det_idx], qualities[det_idx]
            )
            self.tracks[track_idx].last_seen_frame = self.frame_count

        # Second matching: Try to re-identify from lost tracks
        if len(unmatched_dets) > 0 and len(self.lost_tracks) > 0:
            reid_matches = self._reid_match(
                unmatched_dets, bboxes, features, qualities
            )

            for lost_idx, det_idx in reid_matches:
                # Recover lost track
                recovered_track = self.lost_tracks.pop(lost_idx)
                recovered_track.update(
                    bboxes[det_idx], features[det_idx], qualities[det_idx]
                )
                recovered_track.state = 'confirmed'
                recovered_track.time_since_update = 0
                self.tracks.append(recovered_track)

                # Remove from unmatched
                unmatched_dets = [d for d in unmatched_dets if d != det_idx]

        # Mark unmatched tracks as missed
        for track_idx in unmatched_tracks:
            self.tracks[track_idx].mark_missed()

        # Create new tracks for remaining unmatched detections
        for det_idx in unmatched_dets:
            quality = qualities[det_idx] if det_idx < len(qualities) else 0.5
            # Only create track if quality is reasonable
            if quality > 0.3:
                new_track = EnhancedTrack(
                    bboxes[det_idx], features[det_idx], quality=quality
                )
                new_track.last_seen_frame = self.frame_count
                self.tracks.append(new_track)

        # Manage track lifecycle
        self._manage_tracks()

        # Return confirmed tracks with minimum confidence
        outputs = []
        for track in self.tracks:
            if track.state == 'confirmed' and track.get_confidence() > 0.3:
                bbox = track.get_state()
                outputs.append([*bbox, track.track_id])

        return np.array(outputs) if outputs else np.empty((0, 5))

    def _reid_match(self, unmatched_dets, bboxes, features, qualities):
        """Match unmatched detections with lost tracks using Re-ID"""
        if len(self.lost_tracks) == 0:
            return []

        # Get features of lost tracks
        lost_features = np.array([t.get_feature() for t in self.lost_tracks])

        # Get features of unmatched detections
        unmatched_features = features[unmatched_dets]

        # Calculate similarity matrix
        similarity = np.dot(unmatched_features, lost_features.T)

        # Apply threshold
        similarity[similarity < self.reid_threshold] = 0

        # Hungarian matching
        from scipy.optimize import linear_sum_assignment
        cost = 1 - similarity
        row_ind, col_ind = linear_sum_assignment(cost)

        matches = []
        for r, c in zip(row_ind, col_ind):
            if similarity[r, c] > self.reid_threshold:
                matches.append((c, unmatched_dets[r]))

        return matches

    def _manage_tracks(self):
        """Manage track lifecycle with lost track buffer"""
        active_tracks = []

        for track in self.tracks:
            if track.state == 'deleted':
                # Move to lost tracks if it was a good track
                if (track.total_detections > 5 and
                    track.get_confidence() > 0.4 and
                    len(self.lost_tracks) < self.reid_buffer_size):
                    self.lost_tracks.append(track)
            else:
                active_tracks.append(track)

        self.tracks = active_tracks

        # Clean old lost tracks
        current_frame = self.frame_count
        self.lost_tracks = [
            t for t in self.lost_tracks
            if (current_frame - t.last_seen_frame) < 60  # Keep for 60 frames
        ]

    def _match(self, detections, det_features, qualities):
        """Enhanced matching with quality weighting"""
        if len(self.tracks) == 0 or len(detections) == 0:
            return [], list(range(len(detections))), []

        # Filter tracks by confidence
        valid_tracks = [
            i for i, t in enumerate(self.tracks)
            if t.get_confidence() > 0.2
        ]

        if len(valid_tracks) == 0:
            return [], list(range(len(detections))), []

        # Get track predictions and features
        track_bboxes = np.array([self.tracks[i].get_state() for i in valid_tracks])
        track_features = np.array([self.tracks[i].get_feature() for i in valid_tracks])

        det_bboxes = detections[:, :4]

        # Compute IoU distance
        iou_dist = self._iou_distance(track_bboxes, det_bboxes)

        # Compute cosine distance
        cosine_dist = self._cosine_distance(track_features, det_features)

        # Weight by quality
        quality_weights = np.array(qualities).reshape(-1, 1)
        quality_weights = np.clip(quality_weights, 0.3, 1.0)

        # Fused cost with quality weighting
        cost_matrix = (
            self.lambda_iou * iou_dist +
            self.lambda_reid * cosine_dist * quality_weights.T
        )

        # Hungarian algorithm
        from scipy.optimize import linear_sum_assignment
        row_ind, col_ind = linear_sum_assignment(cost_matrix)

        matches = []
        unmatched_tracks = list(range(len(valid_tracks)))
        unmatched_dets = list(range(len(detections)))

        for r, c in zip(row_ind, col_ind):
            if cost_matrix[r, c] < 0.7:
                matches.append((valid_tracks[r], c))
                if r in unmatched_tracks:
                    unmatched_tracks.remove(r)
                if c in unmatched_dets:
                    unmatched_dets.remove(c)

        # Map back to original track indices
        unmatched_tracks = [valid_tracks[i] for i in unmatched_tracks]

        return matches, unmatched_dets, unmatched_tracks


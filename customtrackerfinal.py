class GMC:
    """
    Global Motion Compensation using ORB features and affine transformation.
    Compensates for camera motion to improve Kalman filter predictions.
    """
    def __init__(self, method='orb', downscale=2):
        self.method = method
        self.downscale = downscale
        self.prevFrame = None
        self.prevKeypoints = None
        self.prevDescriptors = None
        self.initializedFirstFrame = False

        # ORB detector
        self.detector = cv2.ORB_create(nfeatures=1000)
        self.matcher = cv2.BFMatcher(cv2.NORM_HAMMING)

    def apply(self, frame, detections=None):
        """
        Estimate camera motion between frames.
        Returns: 2x3 affine transformation matrix (or eye matrix if failed)
        """
        H, W = frame.shape[:2]
        H_ds, W_ds = H // self.downscale, W // self.downscale

        # Downscale frame for efficiency
        frame_ds = cv2.resize(frame, (W_ds, H_ds))
        frame_gray = cv2.cvtColor(frame_ds, cv2.COLOR_BGR2GRAY)

        if not self.initializedFirstFrame:
            self.prevFrame = frame_gray
            self.prevKeypoints, self.prevDescriptors = self.detector.detectAndCompute(frame_gray, None)
            self.initializedFirstFrame = True
            return np.eye(2, 3)

        # Detect features in current frame
        keypoints, descriptors = self.detector.detectAndCompute(frame_gray, None)

        if descriptors is None or self.prevDescriptors is None:
            self.prevFrame = frame_gray
            self.prevKeypoints = keypoints
            self.prevDescriptors = descriptors
            return np.eye(2, 3)

        # Match features
        matches = self.matcher.knnMatch(self.prevDescriptors, descriptors, k=2)

        # Apply ratio test
        good_matches = []
        for match_pair in matches:
            if len(match_pair) == 2:
                m, n = match_pair
                if m.distance < 0.75 * n.distance:
                    good_matches.append(m)

        if len(good_matches) < 10:
            self.prevFrame = frame_gray
            self.prevKeypoints = keypoints
            self.prevDescriptors = descriptors
            return np.eye(2, 3)

        # Extract matched keypoints
        prevPts = np.float32([self.prevKeypoints[m.queryIdx].pt for m in good_matches])
        currPts = np.float32([keypoints[m.trainIdx].pt for m in good_matches])

        # Estimate affine transformation
        transform, inliers = cv2.estimateAffinePartial2D(prevPts, currPts, method=cv2.RANSAC)

        if transform is None:
            transform = np.eye(2, 3)
        else:
            # Scale transform back to original resolution
            transform[:, 2] *= self.downscale

        # Update previous frame
        self.prevFrame = frame_gray
        self.prevKeypoints = keypoints
        self.prevDescriptors = descriptors

        return transform


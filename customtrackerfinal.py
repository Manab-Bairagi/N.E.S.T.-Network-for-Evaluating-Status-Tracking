def create_color_palette(n=100):
    """Generate distinct colors for different track IDs."""
    np.random.seed(42)
    palette = []
    for i in range(n):
        hue = (i * 0.618033988749895) % 1.0  # Golden ratio conjugate
        rgb = tuple(int(x * 255) for x in plt.cm.hsv(hue)[:3])
        palette.append(rgb)
    return palette


COLORS = create_color_palette(100)


def draw_tracks(frame, tracks, show_trajectory=True):
    """
    Draw bounding boxes, IDs, and trajectories on frame.
    Args:
        frame: image array
        tracks: Nx5 array [x1, y1, x2, y2, track_id]
        show_trajectory: whether to draw trajectory trails
    """
    result = frame.copy()

    for track in tracks:
        x1, y1, x2, y2, track_id = track
        x1, y1, x2, y2 = map(int, [x1, y1, x2, y2])
        track_id = int(track_id)

        # Get color for this track ID
        color = COLORS[track_id % len(COLORS)]

        # Draw bounding box
        cv2.rectangle(result, (x1, y1), (x2, y2), color, 2)

        # Draw ID label
        label = f"ID: {track_id}"
        label_size, _ = cv2.getTextSize(label, cv2.FONT_HERSHEY_SIMPLEX, 0.6, 2)
        label_y1 = max(y1, label_size[1] + 10)
        cv2.rectangle(result, (x1, label_y1 - label_size[1] - 10),
                     (x1 + label_size[0], label_y1), color, -1)
        cv2.putText(result, label, (x1, label_y1 - 5),
                   cv2.FONT_HERSHEY_SIMPLEX, 0.6, (255, 255, 255), 2)

    return result


def draw_trajectories(frame, tracker, max_points=30):
    """Draw trajectory trails for all active tracks."""
    result = frame.copy()

    for track in tracker.tracks:
        if track.state == 'confirmed' and len(track.trajectory) > 1:
            color = COLORS[track.track_id % len(COLORS)]
            points = list(track.trajectory)

            # Draw lines between consecutive points
            for i in range(len(points) - 1):
                pt1 = tuple(map(int, points[i]))
                pt2 = tuple(map(int, points[i + 1]))
                thickness = max(1, int(2 * (i + 1) / len(points)))
                cv2.line(result, pt1, pt2, color, thickness)

            # Draw circle at current position
            if len(points) > 0:
                pt = tuple(map(int, points[-1]))
                cv2.circle(result, pt, 4, color, -1)

    return result


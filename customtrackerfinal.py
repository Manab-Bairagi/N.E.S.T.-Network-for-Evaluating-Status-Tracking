class EnhancedTrack(Track):
    """Enhanced track with confidence scoring and better occlusion handling"""

    def __init__(self, bbox, feature, track_id=None, quality=1.0):
        # Initialize parent
        super().__init__(bbox, feature, track_id)

        # Confidence tracking
        self.detection_confidence = 1.0
        self.feature_quality = quality
        self.consecutive_misses = 0

        # Occlusion handling
        self.is_occluded = False
        self.occlusion_frames = 0

        # Re-ID support
        self.last_seen_frame = 0
        self.total_detections = 1

        # Ensure we are using NSA Kalman Filter (double check)
        # Typically initialized in parent, but we want to be sure
        if not isinstance(self.kf, NSAKalmanFilter):
            self.kf = NSAKalmanFilter()
            # Re-init state
            x1, y1, x2, y2 = bbox
            cx, cy = (x1 + x2) / 2, (y1 + y2) / 2
            w, h = x2 - x1, y2 - y1
            self.kf.init(np.array([cx, cy, w, h]))

    def predict(self):
        """Predict with confidence decay and velocity momentum"""
        self.age += 1
        self.time_since_update += 1
        self.consecutive_misses += 1

        # Decay confidence when missing
        self.detection_confidence *= 0.95

        # Check if occluded (missing but high confidence before)
        if self.consecutive_misses > 3 and self.detection_confidence > 0.5:
            self.is_occluded = True
            self.occlusion_frames += 1

        # MOMENTUM LOGIC: If track is lost for > 5 frames, decay velocity
        # This prevents it from floating away if it was moving fast when lost
        if self.time_since_update > 5:
            self.kf.x[4:] *= 0.9

        pred = self.kf.predict()
        return self._xywh_to_xyxy(pred)

    def update(self, bbox, feature, quality=1.0):
        """Update with quality-weighted feature and NSA logic"""
        # 1. Update internal confidence/metrics first

        # Reset counters
        self.consecutive_misses = 0
        self.is_occluded = False
        self.occlusion_frames = 0

        # Update confidence based on quality
        self.detection_confidence = min(1.0, self.detection_confidence + 0.1)
        self.feature_quality = quality
        self.total_detections += 1

        # 2. Call Parent Update (but pass the confidence!)
        # We perform the super().update logic manually here to control the arguments
        # specifically for NSAKalmanFilter

        x1, y1, x2, y2 = bbox
        cx = (x1 + x2) / 2
        cy = (y1 + y2) / 2
        w = x2 - x1
        h = y2 - y1

        # CRITICAL: Pass detection_confidence to NSA Kalman Filter
        # High confidence = tight covariance (trust measurement)
        # Low confidence = loose covariance (trust prediction)
        self.kf.update(np.array([cx, cy, w, h]), confidence=self.detection_confidence)

        self.features.append(feature)
        self.trajectory.append((cx, cy))

        self.hits += 1
        self.time_since_update = 0

        if self.state == 'tentative' and self.hits >= 3:
            self.state = 'confirmed'

    def mark_missed(self):
        """Enhanced missed marking with confidence decay"""
        self.consecutive_misses += 1
        self.detection_confidence *= 0.9

        # More lenient deletion for high-confidence tracks
        if self.state == 'tentative':
            self.state = 'deleted'
        elif self.detection_confidence < 0.2:
            self.state = 'deleted'
        elif self.time_since_update > 30:
            self.state = 'deleted'
        # Give more time to occluded tracks
        elif self.is_occluded and self.time_since_update > 15:
            self.state = 'deleted'

    def get_confidence(self):
        """Get overall track confidence"""
        # Combine detection confidence with track maturity
        maturity_score = min(1.0, self.total_detections / 10.0)
        return self.detection_confidence * 0.7 + maturity_score * 0.3


class LongTermMemory:
    """
    Long-term memory for storing identity embeddings across the entire video.
    Prevents assigning new IDs to previously seen persons.
    """

    def __init__(self, max_identities=200, similarity_threshold=0.50,
                 features_per_id=128, quality_threshold=0.4):
        """
        Args:
            max_identities: Maximum number of identities to remember
            similarity_threshold: Threshold for matching against memory
            features_per_id: Number of features to store per identity
            quality_threshold: Minimum quality to store features in memory
        """
        self.memory = {}  # {track_id: {'features': [], 'last_seen': frame, 'total_appearances': int}}
        self.max_identities = max_identities
        self.similarity_threshold = similarity_threshold
        self.features_per_id = features_per_id
        self.quality_threshold = quality_threshold

    def add_identity(self, track_id, feature, frame_num, quality=1.0):
        """Add or update an identity in memory"""
        # Only store high-quality features
        if quality < self.quality_threshold:
            return

        if track_id not in self.memory:
            self.memory[track_id] = {
                'features': deque([feature], maxlen=self.features_per_id),
                'last_seen': frame_num,
                'total_appearances': 1,
                'first_seen': frame_num
            }
        else:
            self.memory[track_id]['features'].append(feature)
            self.memory[track_id]['last_seen'] = frame_num
            self.memory[track_id]['total_appearances'] += 1

    def search_memory(self, feature, current_frame, max_frames_gap=300):
        """
        Search memory for matching identity

        Args:
            feature: Query feature vector
            current_frame: Current frame number
            max_frames_gap: Maximum gap in frames to consider a match

        Returns:
            matched_track_id or None
        """
        if len(self.memory) == 0:
            return None

        best_match_id = None
        best_similarity = self.similarity_threshold

        for track_id, data in self.memory.items():
            # Skip if identity was seen too long ago (optional filtering)
            # Commenting this out allows matching across entire video duration
            # if (current_frame - data['last_seen']) > max_frames_gap:
            #     continue

            # Calculate average similarity with stored features
            stored_features = np.array(list(data['features']))
            similarities = np.dot(stored_features, feature)
            avg_similarity = np.mean(similarities)
            max_similarity = np.max(similarities)

            # Use weighted score: favor both high average and high max
            combined_score = 0.6 * avg_similarity + 0.4 * max_similarity

            if combined_score > best_similarity:
                best_similarity = combined_score
                best_match_id = track_id

        if best_match_id is not None:
            print(f"  🔍 Memory Match! Recovered ID {best_match_id} (similarity: {best_similarity:.3f})")

        return best_match_id

    def get_identity_stats(self, track_id):
        """Get statistics about an identity"""
        if track_id in self.memory:
            return self.memory[track_id]
        return None

    def cleanup_memory(self, current_frame, max_age=1000):
        """Remove very old identities to prevent memory overflow"""
        if len(self.memory) > self.max_identities:
            # Remove oldest identities that haven't been seen recently
            sorted_ids = sorted(
                self.memory.items(),
                key=lambda x: (x[1]['last_seen'], x[1]['total_appearances'])
            )

            # Keep most recent and most frequently seen
            ids_to_remove = [id for id, _ in sorted_ids[:len(self.memory) - self.max_identities]]

            for id in ids_to_remove:
                del self.memory[id]
                print(f"  🗑️ Removed old identity {id} from memory")

    def get_memory_summary(self):
        """Get summary of memory state"""
        if len(self.memory) == 0:
            return "Memory: Empty"

        total_identities = len(self.memory)
        total_features = sum(len(data['features']) for data in self.memory.values())
        avg_appearances = np.mean([data['total_appearances'] for data in self.memory.values()])

        return f"Memory: {total_identities} IDs, {total_features} features, avg {avg_appearances:.1f} appearances"

